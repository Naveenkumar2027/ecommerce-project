import cloudinary from "../lib/cloudinary.js";
import Product from "../models/product.model.js";

const featuredProductsCache = {
  data: null,
  timestamp: null,
  ttl: 60 * 1000, // cache time to live 1 minute
};

export const getAllProducts = async (req, res) => {
  try {
    const products = await Product.find({});
    res.json({ products });
  } catch (error) {
    console.log("Error in getAllProducts controller", error.message);
    res.status(500).json({ message: "Server error", error: error.message });
  }
};

export const getFeaturedProducts = async (req, res) => {
  try {
    const now = Date.now();
    if (featuredProductsCache.data && (now - featuredProductsCache.timestamp) < featuredProductsCache.ttl) {
      return res.json(featuredProductsCache.data);
    }

    const featuredProducts = await Product.find({ isFeatured: true });

    if (!featuredProducts.length) {
      return res.status(404).json({ message: "No featured products found" });
    }

    featuredProductsCache.data = featuredProducts;
    featuredProductsCache.timestamp = now;

    res.json(featuredProducts);
  } catch (error) {
    console.log("Error in getFeaturedProducts controller", error.message);
    res.status(500).json({ message: "Server error", error: error.message });
  }
};

export const createProduct = async (req, res) => {
  try {
    const { name, description, price, image, category } = req.body;

    let cloudinaryResponse = null;

    if (image) {
      cloudinaryResponse = await cloudinary.uploader.upload(image, { folder: "products" });
    }

    const newProduct = new Product({
      name,
      description,
      price,
      image: cloudinaryResponse?.secure_url || "",
      category,
      isFeatured: false,
    });

    await newProduct.save();

    res.status(201).json(newProduct);
  } catch (error) {
    console.log("Error in createProduct controller", error.message);
    res.status(500).json({ message: "Server error", error: error.message });
  }
};

export const deleteProduct = async (req, res) => {
  try {
    const product = await Product.findById(req.params.id);

    if (!product) {
      return res.status(404).json({ message: "Product not found" });
    }

    if (product.image) {
      const publicId = product.image.split("/").pop().split(".")[0];
      try {
        await cloudinary.uploader.destroy(`products/${publicId}`);
        console.log("deleted image from cloudinary");
      } catch (error) {
        console.log("error deleting image from cloudinary", error);
      }
    }

    await Product.findByIdAndDelete(req.params.id);

    res.json({ message: "Product deleted successfully" });
  } catch (error) {
    console.log("Error in deleteProduct controller", error.message);
    res.status(500).json({ message: "Server error", error: error.message });
  }
};

